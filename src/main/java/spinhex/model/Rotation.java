package spinhex.model;

/**
 * Represents the direction of rotation in the SpinHex game.
 * <p>
 * This enum defines two rotation directions:
 * <ul>
 *   <li>{@code CLOCKWISE} - Rotation in the clockwise direction</li>
 *   <li>{@code COUNTERCLOCKWISE} - Rotation in the counterclockwise direction</li>
 * </ul>
 * </p>
 */
public enum Rotation {
    CLOCKWISE,
    COUNTERCLOCKWISE
}
